//注意转置后按照列优先排列，算法思想是依次将A[][]中属于某列的元素全部取出，添加到B[][]中
void transpose(int A[][], int B[][]) {
	int i, j, k;
	B[0][0] = A[0][0];
	B[0][1] = A[0][2];
	B[0][2] = A[0][1];

    k = 1;
	for (j = 0; j < A[0][2]; ++ j) {
		for (i = 1; i < A[0][0]; ++ i) {
			if (A[i][2] == j) {
				B[k][0] = A[i][0];
				B[k][1] = A[i][2];
				B[k][2] = A[i][1];
				++ k;
			}
		}
	}
}